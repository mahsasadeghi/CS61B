package player;

/**
 * A class that stores a two-dimensional point.
 */
public class Point{
    public int x;/** The x component of the point. */
    public int y;/** The y component of the point. */

    /**
     * A constructor.
     * @param x_ The x component of the point.
     * @parma y_ The y component of the point.
     */
    Point(int x_, int y_){
        x = x_;
        y = y_;
    }
}

